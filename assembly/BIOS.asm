plot4x:
 mov h,0
 ploop:
  push h
  mov h,0
  ploop1:
   dsend b,0x0,0x00  ;set xpos
   dsend c,0x0,0x01  ;set ypos
   dsend f,0x0,0x06  ;plot
   inc c
   inc h
   cmplt h,8
    jmp [ploop1]
  pop h
  sub c,8,c
  inc b
  inc h
  cmplt h,8
   jmp [ploop]
 dsend g,0x0,0x07
 ret

loop0:
 mov h,264 ;height (21*8) 180+167
 mov d,412 ;width (43*8) 

 cmpgt c,h
  jmp [loop0done]
 loop1:
  cmpgt b,d
  jmp [reset_x]
  
  mov g,0
  rbios g,a, f      ;read pixel
  split f,g,f,1     ;split it into lower and upper
  call [plot4x]
  mov f,g
  call [plot4x]
;  sub b,1,b
  dsend g,0x0,0x06
;  add b,1,b
  add a,1,a
  jmp [loop1]
 
 reset_x:
  mov b,68
  add c,8,c
  jmp [loop0]

;waiting...
loop0done:
mov b,1000
mov c,0
tloop:
 dsend e,0x0,0x07 ;render
 gclk a,0
 cmpgt a,b
  jmp [tick]
 jmp [tloop]
tick:
 gclk a,1
 inc c
 add b,1000,b
 cmplt c,6
  jmp [tloop]

mov d,0
mov c,0
mov f,0xF
drecv d,0x0,0x00 ;width
drecv h,0x0,0x01 ;height
ploop0:
 cmpgt c,h
 jmp [loop2done]
ploop1:
 cmpgt b,d
 jmp [preset_x]
 
